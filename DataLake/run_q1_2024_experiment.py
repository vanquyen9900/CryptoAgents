def q1_weeks(start_date: str, end_date: str) -> list[tuple[str, str]]:
    dates = pd.date_range(start_date, end_date, freq="B")
    weeks = []
    for _, group in dates.groupby(dates.isocalendar().week).items():
        weeks.append((group[0].strftime("%Y-%m-%d"), group[-1].strftime("%Y-%m-%d")))
    return weeks


