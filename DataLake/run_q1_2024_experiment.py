def run_cmd(cmd: list[str], *, check: bool = True) -> subprocess.CompletedProcess:
    print("\n$ " + " ".join(cmd))
    return subprocess.run(cmd, check=check)


def load_jsonl(path: Path) -> list[dict]:
    if not path.exists():
        return []
    rows = []
    with path.open("r", encoding="utf-8-sig") as f:
        for line in f:
            if line.strip():
                rows.append(json.loads(line))
    return rows


def write_jsonl(path: Path, rows: list[dict]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", encoding="utf-8") as f:
        for row in rows:
            f.write(json.dumps(row, ensure_ascii=False, default=str) + "\n")


def remove_path(path: Path) -> None:
    if not path.exists():
        return
    print(f"Removing {path}")
    if path.is_dir():
        shutil.rmtree(path)
    else:
        path.unlink()


def clean_outputs() -> None:
    memo_dir = TEST_DATA_DIR / "memo_adaptation"
    remove_path(memo_dir / "trajectories" / "workflow_trajectories.jsonl")
    remove_path(memo_dir / "portfolio_evaluation")
    remove_path(memo_dir / "memory_bank")
    remove_path(REPORT_PATH)


def has_mock_outputs() -> bool:
    memo_dir = TEST_DATA_DIR / "memo_adaptation"
    traj_path = memo_dir / "trajectories" / "workflow_trajectories.jsonl"
    trajectories = load_jsonl(traj_path)
    has_mock_traj = any(MOCK_TRAJ_RE.match(str(row.get("trajectory_id", ""))) for row in trajectories)
    has_mock_report = False
    if REPORT_PATH.exists():
        text = REPORT_PATH.read_text(encoding="utf-8", errors="ignore").lower()
        has_mock_report = "mock pipeline" in text or "generated by mock" in text
    return has_mock_traj or has_mock_report


def copy_file_if_exists(src: Path, dst: Path, label: str) -> None:
    if not src.exists():
        raise FileNotFoundError(f"Missing {label}: {src}")
    dst.parent.mkdir(parents=True, exist_ok=True)
    shutil.copy2(src, dst)
    print(f"Copied {label}: {dst}")


def copy_seed_metadata() -> None:
    src_memo = TRAIN_DATA_DIR / "memo_adaptation"
    dst_memo = TEST_DATA_DIR / "memo_adaptation"
    copy_file_if_exists(
        src_memo / "prompt_sets" / "prompt_sets.jsonl",
        dst_memo / "prompt_sets" / "prompt_sets.jsonl",
        "prompt_sets",
    )
    copy_file_if_exists(
        src_memo / "memory_policies" / "memory_policies.json",
        dst_memo / "memory_policies" / "memory_policies.json",
        "memory_policies",
    )
    copy_file_if_exists(
        src_memo / "context_policies" / "context_policies.json",
        dst_memo / "context_policies" / "context_policies.json",
        "context_policies",
    )


def ensure_test_split_ready() -> None:
    memo_dir = TEST_DATA_DIR / "memo_adaptation"
    episodes = memo_dir / "episodes" / "trading_episodes.jsonl"
    inputs = memo_dir / "materialized_inputs" / f"inputs_{CONTEXT_POLICY_ID}.jsonl"
    if episodes.exists() and inputs.exists():
        print("Q1 episodes and materialized inputs already exist.")
        return

    print("Q1 episodes/materialized inputs missing. Rebuilding from already-crawled test data.")
    run_cmd(
        [
            sys.executable,
            str(DATALAKE_DIR / "run_test_2024_q1_crawl.py"),
            "--skip-crawl",
        ]
    )
    if not episodes.exists() or not inputs.exists():
        raise FileNotFoundError(
            "Q1 test split build did not produce required files: "
            f"episodes={episodes.exists()} inputs={inputs.exists()}"
        )


def seed_arm_b_memory() -> None:
    src_path = TRAIN_DATA_DIR / "memo_adaptation" / "memory_bank" / "memo_memory_bank.jsonl"
    dst_path = TEST_DATA_DIR / "memo_adaptation" / "memory_bank" / "memo_memory_bank.jsonl"
    source_rows = load_jsonl(src_path)
    existing_rows = load_jsonl(dst_path)

    existing_ids = {row.get("memory_id") for row in existing_rows}
    seeded = []
    for row in source_rows:
        if row.get("memory_bank_version") != SEED_2022_MEMORY_VERSION:
            continue
        new_row = dict(row)
        new_row["source_memory_bank_version"] = SEED_2022_MEMORY_VERSION
        new_row["memory_bank_version"] = ARM_B_MEMORY_VERSION
        new_row["memory_id"] = f"{row.get('memory_id')}_seed2022"
        new_row["visible_from"] = f"{START_DATE}T00:00:00Z"
        if new_row["memory_id"] not in existing_ids:
            seeded.append(new_row)
            existing_ids.add(new_row["memory_id"])

    write_jsonl(dst_path, existing_rows + seeded)
    print(f"Seeded Arm B with {len(seeded)} 2022 memories.")


