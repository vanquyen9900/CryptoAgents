def main() -> None:
    global TEST_DATA_DIR
    parser = argparse.ArgumentParser(description="Run the real Q1-2024 MeMo portfolio experiment.")
    parser.add_argument(
        "--arm",
        choices=["A", "B", "C", "ALL", "EVAL", "EVAL_A", "EVAL_B", "EVAL_C"],
        default="ALL",
        help="Which arm to run. EVAL evaluates all arms. EVAL_A, EVAL_B, EVAL_C evaluate specific arms only.",
    )
    parser.add_argument(
        "--clean",
        action="store_true",
        help="Remove existing Q1 trajectories, memory_bank, portfolio_evaluation, and report before running.",
    )
    parser.add_argument(
        "--skip-prerun-contract",
        action="store_true",
        help="Skip the pre-run readiness contract.",
    )
    parser.add_argument(
        "--start-date",
        default=START_DATE,
        help=(
            "Start date for the experiment window (YYYY-MM-DD). "
            f"Default: {START_DATE} (beginning of Q1-2024)."
        ),
    )
    parser.add_argument(
        "--end-date",
        default=END_DATE,
        help=(
            "End date for the experiment window (YYYY-MM-DD). "
            f"Default: {END_DATE} (end of Q1-2024). "
            "Warning: dates beyond the crawled data range may yield empty episodes."
        ),
    )
    parser.add_argument(
        "--data-dir",
        default=None,
        help="Optional override for the test data directory. If not provided, defaults to DataLake/data_test_2024_q1.",
    )
    parser.add_argument(
        "--prompt-set-ids",
        nargs="+",
        default=["ps_default_v1", "ps_macro_defensive_v1", "ps_risk_aware_v1"],
        help="Specific prompt sets to run. Defaults to all 3.",
    )
    args = parser.parse_args()

    global PROMPT_SETS
    PROMPT_SETS = args.prompt_set_ids

    if args.data_dir:
        TEST_DATA_DIR = Path(args.data_dir).resolve()

    start_date: str = args.start_date
    end_date: str = args.end_date

    # Validation: warn if requested window goes beyond the crawled data range
    if end_date > END_DATE:
        print(
            f"[WARNING] --end-date {end_date} is beyond the crawled data range "
            f"(max: {END_DATE}). Episodes outside the crawled range will be empty."
        )
    if start_date < START_DATE:
        print(
            f"[WARNING] --start-date {start_date} is before the crawled data range "
            f"(min: {START_DATE}). Episodes before that date will be empty."
        )
    if start_date > end_date:
        raise SystemExit(
            f"[ERROR] --start-date {start_date} must not be after --end-date {end_date}."
        )

    print(f"Experiment window: {start_date} → {end_date}")

    if args.clean:
        clean_outputs()
    elif not args.arm.startswith("EVAL") and has_mock_outputs():
        raise SystemExit(
            "Existing mock/demo Q1 outputs detected. Re-run with --clean before starting a real run, "
            "or archive them manually."
        )

    if not args.arm.startswith("EVAL"):
        copy_seed_metadata()
        ensure_test_split_ready()
        if not args.skip_prerun_contract:
            run_cmd(
                [
                    sys.executable,
                    str(DATALAKE_DIR / "tools" / "contracts" / "test_memo_q1_prerun_contract.py"),
                    "--data-dir",
                    str(TEST_DATA_DIR),
                    "--tournament-id",
                    TOURNAMENT_ID,
                    "--start-date",
                    start_date,
                    "--end-date",
                    end_date,
                    "--symbols",
                    *SYMBOLS,
                    "--allow-existing-outputs",
                ]
            )

    if args.arm in {"ALL", "A"}:
        run_arm_a(start_date, end_date)
    if args.arm in {"ALL", "B"}:
        run_arm_b(start_date, end_date)
    if args.arm in {"ALL", "C"}:
        run_arm_c(start_date, end_date)
    if args.arm in {"ALL", "EVAL"}:
        run_eval_and_contracts(start_date, end_date)
    elif args.arm == "EVAL_A":
        run_evaluator(GROUP_A, start_date, end_date)
        write_summary_report()
        print("\n[INFO] Partial evaluation complete for Arm A. Skipping full contract tests.")
    elif args.arm == "EVAL_B":
        run_evaluator(GROUP_B, start_date, end_date)
        write_summary_report()
        print("\n[INFO] Partial evaluation complete for Arm B. Skipping full contract tests.")
    elif args.arm == "EVAL_C":
        run_evaluator(GROUP_C, start_date, end_date)
        write_summary_report()
        print("\n[INFO] Partial evaluation complete for Arm C. Skipping full contract tests.")


if __name__ == "__main__":
    main()
