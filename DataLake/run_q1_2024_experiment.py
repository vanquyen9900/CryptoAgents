def write_summary_report() -> None:
    REPORT_PATH.parent.mkdir(parents=True, exist_ok=True)
    lines = [
        "# Q1 2024 Portfolio Evaluation Report",
        "",
        "Generated from real trajectory artifacts by `run_q1_2024_experiment.py`.",
        "",
    ]
    for group in [GROUP_A, GROUP_B, GROUP_C]:
        metrics_path = (
            TEST_DATA_DIR
            / "memo_adaptation"
            / "portfolio_evaluation"
            / group
            / "portfolio_metrics.jsonl"
        )
        rows = load_jsonl(metrics_path)
        lines.extend([f"## {group}", ""])
        if rows:
            df = pd.DataFrame(rows)
            try:
                lines.append(df.to_markdown(index=False))
            except ImportError:
                lines.extend(["```text", df.to_string(index=False), "```"])
        else:
            lines.append("_No metrics generated._")
        lines.append("")
    REPORT_PATH.write_text("\n".join(lines), encoding="utf-8")
    print(f"Wrote {REPORT_PATH}")


